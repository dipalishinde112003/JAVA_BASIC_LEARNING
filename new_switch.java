public class new_switch
{
    public static void main(String[] args) {
        String day="monday";
        switch(day)
        {
            case "monday","sunday":
            System.out.println("hi");
            break;
            case "tuesday":
            System.out.println("bye");
            break;
            default:
            System.out.println("end");
        }
    }
    
}
