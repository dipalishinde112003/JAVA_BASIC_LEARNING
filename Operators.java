public class Operators {
    public static void main(String[] args) {
        int n1=6;
        int n2=4;
        int r=n1+n2;
        System.out.println(r);
        System.out.println(n1-n2);
        System.out.println(n1*n2);
        System.out.println(n1/n2);
        System.out.println(n1%n2);
    }
}
